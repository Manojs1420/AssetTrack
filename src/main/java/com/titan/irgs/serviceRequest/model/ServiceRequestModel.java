package com.titan.irgs.serviceRequest.model;

import lombok.AllArgsConstructor;
import lombok.Data;
import lombok.NoArgsConstructor;

@Data
@AllArgsConstructor
@NoArgsConstructor
public class ServiceRequestModel {

	private Long serviceRequestId;

	private Long srEscalationId;

	public Long getServiceRequestId() {
		return serviceRequestId;
	}

	public void setServiceRequestId(Long serviceRequestId) {
		this.serviceRequestId = serviceRequestId;
	}

	public Long getSrEscalationId() {
		return srEscalationId;
	}

	public void setSrEscalationId(Long srEscalationId) {
		this.srEscalationId = srEscalationId;
	}

}
