package com.titan.irgs.serviceRequest.domain;

import java.util.Date;

import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.Temporal;
import javax.persistence.TemporalType;

@Entity
public class EscalationLevel {
	

	
	@Id
	@GeneratedValue(strategy = GenerationType.IDENTITY)
	private Long escalationLevelId;

	private String escalationLevelName;
	
	@Temporal(TemporalType.TIMESTAMP)
	private Date CreatedDate;

	public Long getEscalationLevelId() {
		return escalationLevelId;
	}

	public void setEscalationLevelId(Long escalationLevelId) {
		this.escalationLevelId = escalationLevelId;
	}

	public String getEscalationLevelName() {
		return escalationLevelName;
	}

	public void setEscalationLevelName(String escalationLevelName) {
		this.escalationLevelName = escalationLevelName;
	}

	public Date getCreatedDate() {
		return CreatedDate;
	}

	public void setCreatedDate(Date createdDate) {
		CreatedDate = createdDate;
	}


	

	

	


	
	
	
	





}
